package trapezio;

public class Trapezio {

	public static void main (String[] args) {
		
		//Cálculo da Área do Trapézio//
		
		float área;
		float base_maior;
		float base_menor;
		float altura;
		
		base_maior = 15.5f;
		base_menor = 8f;
		altura = 4.5f;
		
		área = (base_maior + base_menor) * altura / 2;
		
		System.out.print("a ÁREA do Trapézio é igual a " + área);
	}
	
}
